/**
 * 日期字符串转换为可比较的Date对象
 * @param {string} dateStr - 格式为 'YYYY.MM.DD' 的日期字符串
 * @returns {Date} Date对象
 */
function parseNewsDate(dateStr) {
  const [year, month, day] = dateStr.split('.')
  return new Date(parseInt(year), parseInt(month) - 1, parseInt(day))
}

/**
 * 新闻数据排序函数
 * 按日期降序排列（最新的在前面）
 * @param {Array} newsArray - 新闻数据数组
 * @returns {Array} 排序后的新闻数组
 */
function sortNewsByDate(newsArray) {
  return [...newsArray].sort((a, b) => {
    const dateA = parseNewsDate(a.date)
    const dateB = parseNewsDate(b.date)
    return dateB - dateA // 降序排列
  })
}

/**
 * 获取排序后的新闻数据
 * @returns {Array} 按日期排序的新闻数组
 */
export function getSortedNewsData() {
  return sortNewsByDate(rawNewsData)
}

/**
 * 根据ID获取单条新闻数据
 * @param {string} newsId - 新闻ID
 * @returns {Object|null} 新闻数据对象或null
 */
export function getNewsById(newsId) {
  const sortedNews = getSortedNewsData()
  return sortedNews.find(news => news.id === newsId) || null
}

/**
 * 获取最新的N条新闻
 * @param {number} count - 需要获取的新闻数量
 * @returns {Array} 最新的新闻数组
 */
export function getLatestNews(count = 5) {
  const sortedNews = getSortedNewsData()
  return sortedNews.slice(0, count)
}

